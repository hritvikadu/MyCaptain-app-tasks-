f=input("Input the file name:")
s= f.split('.')[1]
if s=="py":
    print("The extension of the file is: 'python'")

elif s=="js":
     print("The extension of the file is: 'JavaScript'")

elif s=="htm" or s=="html":
     print("The extension of the file is: 'HTML'")

elif s=="jpeg" or s=="jpg":
     print("The extension of the file is: 'JPEG'")
